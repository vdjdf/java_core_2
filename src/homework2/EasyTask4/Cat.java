package homework2.EasyTask4;

public class Cat {
    private int kg;
    private String name;

    public Cat(int kg, String name) {
        this.kg = kg;
        this.name = name;

    }

    public int getKg() {
        return kg;
    }

    public void setKg(int kg) {
        this.kg = kg;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
