package homework2.EasyTask4;

public class Dog {
    private int year;
    private String color;


    public Dog(int year, String color) {
        this.year = year;
        this.color = color;

    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
